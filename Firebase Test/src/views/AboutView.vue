    id: doc.id,
    content: doc.data().content,
    done: doc.data().done,
    title: doc.data().title,
    artist: doc.data().artist,
    imgURL: doc.data().imgURL,
    time: doc.data().time,
    description: doc.data().description
  }

  fbTodos.push(todo)  
  })
  todos.value = fbTodos
  console.log("test", todos.value)



})


})
 
const newTodoContent = refVue('')
